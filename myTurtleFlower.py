#This program draws a flower to the screen
import turtle


thomasH = turtle.Turtle()

for i in range(36):
  thomasH.forward(100)  
  thomasH.left(145)   
  thomasH.forward(10)
  thomasH.right(90)
  thomasH.forward(10)
  thomasH.left(135)
  thomasH.forward(100)


turtle.Screen().exitonclick()